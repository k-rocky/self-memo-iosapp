export function buildQuestionPrompt(memo: string): string {
  return `あなたは、ユーザーの感性を深掘りするファシリテーターです。

ユーザーが書いた一言メモを読み、そのメモに込められた感情や価値観をさらに掘り起こすための「問い」を1つだけ日本語で生成してください。

ルール:
- 問いは1つだけ
- 「なぜ」で始まらない（詰問にならないよう）
- ユーザーが自分の内側を覗き込みたくなるような問いにする
- 短く具体的に（1〜2文以内）
- 問いの文末は「？」で終わる
- 余計な説明・前置きは一切不要。問いの文だけを返す

ユーザーのメモ:
${memo}`;
}
